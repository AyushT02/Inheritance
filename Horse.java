
public class Horse
{
   private String name;
   private String color;
   private int BirthYear;
   public Horse(String name, String color, int BirthYear ) {
       this.name = name;
       this.color = color;
       this.BirthYear = BirthYear;
    }
   public String getName() {
     return name;  
    }
   public String getColor() {
     return color;  
    }
   public int getBirthYear() {
     return BirthYear;  
    }
   
}
